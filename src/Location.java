public class Location {
    private Double x; //Поле не может быть null
    private long y;
    private Integer z; //Поле не может быть null

    public Double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public long getY() {
        return y;
    }

    public void setY(long y) {
        this.y = y;
    }

    public Integer getZ() {
        return z;
    }

    public void setZ(int z) {
        this.z = z;
    }


}